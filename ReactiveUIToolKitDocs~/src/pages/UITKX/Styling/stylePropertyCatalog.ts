/**
 * Complete catalog of every style property in ReactiveUIToolKit.
 * Used by StylingPage to render consistent property cards.
 *
 * Each entry has:  key, name, type, description, typed example, untyped example,
 *                  related CssHelpers, category, version, shorthand info.
 *
 * Sorted by category ├втАатАЩ version ├втАатАЩ alphabetical.
 */

export interface PropertyCard {
  /** camelCase key used in Style dictionary, e.g. "flexGrow" */
  key: string
  /** PascalCase name used in typed Style API, e.g. "FlexGrow" */
  name: string
  /** C# type accepted by the typed property, e.g. "StyleFloat" */
  type: string
  /** One-line description */
  description: string
  /** Typed Style API example */
  typedExample: string
  /** Untyped tuple syntax example */
  untypedExample: string
  /** Related CssHelper names */
  helpers?: string[]
  /** Category for grouping */
  category: PropertyCategory
  /** Unity version where this was introduced (omit for floor) */
  sinceUnity?: string
  /** True if this is a shorthand that sets multiple properties */
  shorthand?: boolean
}

